import React from 'react';

const Starts = ({star}) => {


    const stars = {
        one : "⭐_ _ _",
        two: "⭐⭐_ _",
        three:"⭐⭐⭐_",
        four:"⭐⭐⭐⭐"
    }
    if (star <= 5){
        return (<h1>{stars.one}</h1>)
    }
    if(star >= 6 && star < 10){
        return (<h1>{stars.two}</h1>)}
    if(star >= 10 && star < 20){
        return (<h1>{stars.three}</h1>)}  
        
    if(star >= 20){
        return ( <h1>{stars.four}</h1> )}

       
       
       

    
};

export default Starts;